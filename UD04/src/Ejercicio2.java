
public class Ejercicio2 {
	public static void main(String args[]) {
		int n = 5;
		double a = 4.56;
		char c = 'a';
		
		System.out.println(n + " " + a + " " + c);	//Todos los valores
		System.out.println(n+a); //Suma de n + a
		System.out.println(a-n); //Diferencia a-n
		System.out.println((int) c); //Valor del caracter a
	}
}
