
public class Ejercicio3 {
	public static void main(String args[]) {
		
		int x = 1;
		int y = 2;
		double n = 4.5;
		double m = 5.5;
		
		System.out.println(x + " " + y + " " + n + " " + m); 			//Todos los valores
		
		System.out.println(x+y); 										//Suma xy
		System.out.println(x-y);										//Resta xy
		System.out.println(x*y); 										//Producto xy
		System.out.println(x/y); 										//Cuociente xy
		System.out.println(x%y); 										//Resto xy
		
		System.out.println(n+m); 										//Suma nm
		System.out.println(n-m); 										//Resta nm
		System.out.println(n*m); 										//Producto nm
		System.out.println(n/m); 										//Cuociente nm
		System.out.println(n%m); 										//Resto nm
		
		System.out.println(x+n); 										//Suma xn
		System.out.println(y/m); 										//Cuociente y/m
		System.out.println(y%m); 										//Resto ym
		
		System.out.println(x*2 + " " + y*2 + " " + n*2 + " " + m*2); 	//Doble
		System.out.println(x+y+n+m); 									//Suma
		System.out.println(x*y*n*m); 									//Producto
	}
}
